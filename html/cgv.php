        <section id="cgu" class="mentions_legales-section">
            <h3 class="mentions_legales-title">CONDITIONS GÉNÉRALES DE VENTE (CGV) DE LA PLATEFORME PACT</h3>

            <h4>1. Objet des CGV</h4>
            <p class="mentions_legales-paragraph">
                Les présentes Conditions Générales de Vente (CGV) ont pour objet de définir les conditions de vente des services proposés par la plateforme PACT aux professionnels du tourisme. 
            <p class="mentions_legales-paragraph">
                Ces services permettent la publication et la promotion d’offres touristiques, avec des options gratuites et payantes.
            </p>

            <h4>2. Offre de services</h4>
            <p class="mentions_legales-paragraph">
                PACT propose deux types d’offres :
            </p>
            <ul class="mentions_legales-list">
                <li>Offre Gratuite : disponible pour les professionnels du secteur public et associatif, sans frais de promotion.</li>
                <li>Offres Payantes : accessibles aux professionnels du secteur privé, avec deux niveaux de souscription :</li>
                <ul class="mentions_legales-sous-list">
                    <li>Offre Standard : inclut la publication de l’offre sur la plateforme.</li>
                    <li>Offre Premium : offre des avantages supplémentaires, notamment le droit de blacklistage de trois avis par an.</li>
                </ul> 
            </ul>
            <p>
                Des options promotionnelles sont également disponibles pour mettre en avant les offres :
            </p>
            <ul class="mentions_legales-list">
                <li>Option "En Relief" : mise en évidence des offres dans les listes.</li>
                <li>Option "À la Une" : promotion sur la page d’accueil.</li>
            </ul>

            <h4>3. Tarification et paiement</h4>
            <p class="mentions_legales-paragraph">
                Les tarifs des offres payantes et des options sont définis sur la plateforme.
            </p>

            <h5>3.1. Facturation des Offres</h5>
            <p class="mentions_legales-paragraph">
                La facturation est mensuelle et réalisée le 1er jour du mois suivant. Le montant facturé dépend du nombre de jours pendant lesquels l’offre est restée en ligne.
                <ul class="mentions_legales-list">
                    <li>Une offre mise hors ligne est facturée jusqu’au lendemain de sa désactivation.</li>
                    <li>Une offre remise en ligne est facturée à partir du jour de sa réactivation.</li>
                </ul>
            </p>

            <h5>3.2. Facturation des Options Promotionnelles</h5>
            <p class="mentions_legales-paragraph">
                Les options "En Relief" et "À la Une" sont facturées à la semaine, avec un engagement minimal d’une semaine et un maximum de quatre semaines. La facturation commence à la date d’activation, même si l’option est annulée ensuite.
            </p>

            <h4>4. Modalités de paiement</h4>
            <p class="mentions_legales-paragraph">
                Le paiement s’effectue par prélèvement automatique sur les coordonnées bancaires fournies lors de la création du compte.
            </p>

            <p class="mentions_legales-paragraph">
                En cas d’échec de prélèvement, PACT se réserve le droit de suspendre l’accès aux services payants jusqu’à régularisation.
            </p>


            <h4>5. Conditions de résiliation</h4>
            <h5>5.1. Résiliation des Offres</h5>
            <p class="mentions_legales-paragraph">
                Les professionnels peuvent désactiver leurs offres à tout moment. Une offre mise hors ligne ne génère plus de frais, mais les montants déjà facturés restent dus.
            </p>

             <h5>5.2. Suppression de compte</h5>
            <p class="mentions_legales-paragraph">
                La suppression d’un compte professionnel est soumise à la régularisation de toutes les factures en cours. Elle est effectuée par l’administrateur de PACT.
            </p>

            <h4>6. Droit de rétractation</h4>
            <p class="mentions_legales-paragraph">
                Conformément au Code de la consommation, les professionnels souscrivant des services à distance ne bénéficient pas du droit de rétractation.
            </p>

            <h4>7. Responsabilité</h4>
            <p class="mentions_legales-paragraph">
                PACT ne saurait être tenue responsable des pertes de revenus ou des dommages indirects consécutifs à l’utilisation des services. En cas de dysfonctionnement, la responsabilité de PACT est limitée aux montants effectivement facturés.
            </p>

            <h4>8. Modification des CGV</h4>
            <p class="mentions_legales-paragraph">
                PACT se réserve le droit de modifier les présentes CGV. Les nouvelles CGV entrent en vigueur à compter de leur publication sur le site. L’utilisation continue des services vaut acceptation des modifications.
            </p>

            <h4>9. Loi applicable et juridiction</h4>
            <p class="mentions_legales-paragraph">
                Les présentes CGV sont soumises au droit français. En cas de litige, les tribunaux compétents seront ceux du ressort du siège social de PACT.
            </p>
